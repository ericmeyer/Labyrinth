-(id)init
{
	[super init];
	myX = 0.0f;
	myY = 0.0f;
	myX = 0.0f;
}

@implementation UIAcceleration

-(double) getx
{
	return myX;
}

-(double) gety
{
	return myX;
}

-(double) getz
{
	return myX;
}

@end
